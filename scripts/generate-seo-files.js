import fs from 'node:fs';
import path from 'node:path';

const ROOT = process.cwd();
const PUBLIC_DIR = path.join(ROOT, 'public');

const SITE_URL = 'https://klasiz.fun';
const TODAY = new Date().toISOString().slice(0, 10);

const PUBLIC_ROUTES = [
  { path: '/', changefreq: 'daily', priority: '1.0' },
  { path: '/about', changefreq: 'monthly', priority: '0.7' },
  { path: '/faq', changefreq: 'weekly', priority: '0.8' },
  { path: '/privacy', changefreq: 'yearly', priority: '0.4' },
  { path: '/terms', changefreq: 'yearly', priority: '0.4' }
];

const PRIVATE_PREFIXES = [
  '/api/',
  '/admin/',
  '/dashboard',
  '/portal',
  '/settings',
  '/lesson-planner',
  '/setup',
  '/egg',
  '/torenado',
  '/tornado',
  '/faceoff',
  '/memorymatch',
  '/quiz',
  '/motorace',
  '/horserace',
  '/spelltheword',
  '/auth/'
];

function ensurePublicDir() {
  if (!fs.existsSync(PUBLIC_DIR)) {
    fs.mkdirSync(PUBLIC_DIR, { recursive: true });
  }
}

function generateSitemapXml() {
  const rows = PUBLIC_ROUTES.map((route) => {
    const loc = `${SITE_URL}${route.path}`;
    return [
      '  <url>',
      `    <loc>${loc}</loc>`,
      `    <lastmod>${TODAY}</lastmod>`,
      `    <changefreq>${route.changefreq}</changefreq>`,
      `    <priority>${route.priority}</priority>`,
      '  </url>'
    ].join('\n');
  }).join('\n\n');

  return [
    '<?xml version="1.0" encoding="UTF-8"?>',
    '<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9">',
    rows,
    '</urlset>',
    ''
  ].join('\n');
}

function generateRobotsTxt() {
  const disallowLines = PRIVATE_PREFIXES.map((prefix) => `Disallow: ${prefix}`);
  return [
    '# Auto-generated by scripts/generate-seo-files.js',
    '# Domain: klasiz.fun',
    '',
    'User-agent: *',
    'Allow: /',
    ...disallowLines,
    '',
    `Sitemap: ${SITE_URL}/sitemap.xml`,
    'Host: klasiz.fun',
    ''
  ].join('\n');
}

function writeFile(targetPath, content) {
  fs.writeFileSync(targetPath, content, 'utf8');
  // eslint-disable-next-line no-console
  console.log(`updated ${path.relative(ROOT, targetPath)}`);
}

function run() {
  ensurePublicDir();

  const sitemapPath = path.join(PUBLIC_DIR, 'sitemap.xml');
  const robotsPath = path.join(PUBLIC_DIR, 'robots.txt');

  writeFile(sitemapPath, generateSitemapXml());
  writeFile(robotsPath, generateRobotsTxt());

  // eslint-disable-next-line no-console
  console.log('SEO files generated for klasiz.fun');
}

run();
